		<div class="row well" id="forma">
			<div class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
				<h4 >¿QUÉ FORMA UNA EMPRESA?</h4>
				<ul class="list-group">
					<li class="list-group-item">
						<strong>
							PERSONAS
						</strong>
						<ul>
							<li>
								PROPIETARIOS
								<ul>
									<li>
										Los dueños de la empresa
										<ul>
											<li>
												Aportan capital o bienes para su creación o lacompran
											</li>
											<li>
												De uno a millones
											</li>
										</ul>
									</li>
									<li>
										Toman las grandes decisiones
										<ul>
											<li>
												Toman las grandes decisiones
												</br>
												"Qué vamos a hacer"
											</li>
										</ul>
									</li>
									<li>
										Asumen un riesgo económico (inversión y/o patrimonio)
									</li>
								</ul>
							</li>
							<li>
								GERENTES
								<ul>
									<li>
										Dirigentes de la empresa
										<ul>
											<li>
												Trabajadores contratados por los propietarios
											</li>
											<li>
												Directores, gerentes o administradores
											</li>
											<li>
												Formación universitaria o gran experiencia
											</li>
										</ul>
									</li>
									<li>
										Deciden el camino para cumplir los objetivos marcados
										<ul>
											<li>
												Gestiona: estructura organizativa, procedimientos, procesos, recursos
												</br>
												"Cómo lo vamos a hacer"
											</li>
										</ul>
									</li>
									<li>
										Asumen un riesgo profesional (prestigio y empleo)
									</li>
								</ul>
							</li>
							<li>
								TRABAJADORES
								<ul>
									<li>
										Trabajan para la empresa
										<ul>
											<li>
												Contratados por los gerentes
											</li>
											<li>
												Relación contractual trabajador/empresa (regulación Estatutos de los Trabajadores o Convenio Colectivo)
											</li>
											<li>
												Importa su buena elección para el puesto a desempeñar
											</li>
										</ul>
									</li>
									<li>
										Llevan a cabo las tareas diseñadas por los gerentes
									</li>
									<li>
										Asumen riesgo profesional (empleo)
									</li>
								</ul>
							</li>
						</ul>
					</li>
					<li class="list-group-item">
						<strong>
							BIENES, DERECHOS Y OBLIGACIONES
						</strong>
						</br>
						<small>
							Todo lo que la empresa tiene en propiedad(dinero líquido, en depósito e inversiones) y sus deudas
						</small>
						<ul>
							<li>
								ACTIVO NO CORRIENTE<small> Todos los bienes que se esperan que permanezcan un tiempo relativamente largo</small>
								<ul>
									<li>
										Bienes para el normal funcionamiento de la empresa
									</li>
									<li>
										Inversiones a largo plazo
									</li>
									<li>
										Bienes tangibles: máquinas, naves, vehículos
									</li>
									<li>
										Bienes intangibles: patentes, programas, acciones
									</li>
								</ul>
							</li>
							<li>
								ACTIVO CORRIENTE<small> Todos los bienes que se esperan que permanezcan poco tiempo</small>
								<ul>
									<li>
										Dinero líquido (en la empresa, banco)
									</li>
									<li>
										Derechos de cobro y depósitos a corto plazo
									</li>
									<li>
										Bienes a vender (no son existencias)
									</li>
									<li>
										Existencias: productos terminados, mercaderias, materias primas...
									</li>
								</ul>
							</li>
							<li>
								OBLIGACIONES<small> Combinación de personas y recursos(bienes) para conseguir los objetivos establecidos</small>
								<ul>
									<li>
										Pasos:
										<ul>
											<li>
												Los propietarios establecen los objetivos
											</li>
											<li>
												El gerente establece cómo se van a lograr
											</li>
											<li>
												Se coordina y asigna funciones a los trabajadores para conseguir los objetivos
											</li>
											<li>
												El gerente ordena, dirige y realiza un seguimiento de la actividad. Actúa en consecuencia a los resultados obtenidos (contratación, despido, cambio de organización, pide cambio de objetivos (a los propietarios))
											</li>
										</ul>
									</li>
									<li>
										Organigrama: representación gráfica de la estructura organizativa
									</li>
								</ul>
							</li>
						</ul>
					</li>
					<li class="list-group-item">
						<strong>
							EL ENTORNO
						</strong>
						</br>
						<small>
							Todo aquello que rodea a la empresa y que ejerce una influencia sobre ella
						</small>
						<ul>
							<li>
								COMPETENCIA
								<ul>
									<li>Empresas que venden un producto que puede sustituir al que vende la empresa</li>
									<li>Tener en cuenta sus precios, productos, calidad, servicios adicionales</li> 
								</ul>
							</li>
							<li>
								CLIENTES
								<ul>
									<li>Público objetivo de nuestro producto</li>
									<li>Vital para la supervivencia de la empresa: se debe conocer, servir y motivar al cliente</li>
								</ul>
							</li>
							<li>
								NORMATIVA
								<ul>
									<li>
										Respetar normas establecidas por los Poderes Públicos
									</li>
									<li>
										Estatuto de los Trabajadores, Convenios, Ley de Prevención de Riesgos Laborales, Ley General de Seguridad Social, normas fiscales y normativas municipales
									</li>
								</ul>
							</li>
							<li>
								ORGANISMOS PÚBLICOS
								<ul>
									<li>
										Crean las normas, vigilan su cumplimiento, asesoran y ayudan
									</li>
									<li>
										Inspectores y jueces
									</li>
								</ul>
							</li>
							<li>
								SOCIEDAD
								<ul>
									<li>
										Cultura, creencias y valores de la sociedad de la que la empresa forma parte
									</li>
									<li>
										Consideración de tendencias y modas del momento concreto
									</li>
								</ul>
							</li>
							<li>
								SITUACIÓN ECONÓMICA Y PREVISIONES FUTURAS
								<ul>
									<li>
										El funcionamiento de la empresa se debe ajustar al momento económico actual
									</li>
								</ul>
							</li>							
						</ul>
					</li>
				</ul>
			</div>
