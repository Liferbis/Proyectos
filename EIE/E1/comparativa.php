							</li>
						</ul>
					</li>
					<li class="list-group-item">
						<strong>
							SOCIALES
						</strong>
						<ul>
							<li>
								Las empresas forman parte de la sociedad y tienen unas obligaciones para con ella
								<ul>
									<li>
										Satisfacer las necesidades de las personas:
										<small>
												Ofrecer bienes y servicios para cubrir esas necesidades
										</small>
									</li>
									<li>
										Generar riqueza al país:
										<ul>
											<li>
												Pago de salarios (Flujo circular de la renta)
