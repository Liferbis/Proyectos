			<h4>Fuentes PROPIAS de financiacion</h4>
			<ul class="list-group">
				<li class="list-group-item">
					Son aquellas que provienen de la propia empresa o de las aportaciones que hacen los socios. Todas ellas son no exigibles y forman parte del pasivo de la empresa en la masa Neto Patrimonial o Pasivo no Exigible
				</li>
				<li class="list-group-item">
					TIPOS:
					<ul>
						<li class="list-group-item">
							<strong>
								1.	Beneficios aplicados 
							</strong>
							<ul>
								<li>
									Las ganancias que obtiene la empresa al final de un ejercicio económico se pueden utilizar de diferente forma:
									<ul>
										<li>
											pago de impuestos (obligatorio)
										</li>
										<li>
											reparto de beneficios a los socios
										</li>
										<li>
											guardarlo sin aplicación alguna
										</li>
										<li>
											incluirlo en las reservas 

										</li>
										<li>
											reinvertirlo en la empresa
										</li>
										<li>...</li>
									</ul>
								</li>
								<li>
									Nosotros aquí nos fijamos en esta última utilidad que tiene como objeto renovar, amantener y ampliar los medios de la empresa para continuar funcionado
								</li>
								<li>
									Cuando la empresa marcha bien esta fuente puede ser la única que necesite la empresa, pero esto ocurre en muy contadas ocasiones
