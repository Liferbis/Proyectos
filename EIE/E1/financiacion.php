		<div class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
			<h4 > LAS FUENTES DE FINANCIACIÓN </h4>
			<ul class="list-group">
				<li class="list-group-item">
					Cuando hablamos de fuentes de financiación nos estamos refiriendo al <strong>origen</strong> que pueden tener los recursos económicos que necesita la empresa en un momento determinado 
				</li>
				<li class="list-group-item">
					Pueden venir de la propia empresa o del exterior, o lo que es lo mismo, hablaremos de
					<ul>
						<li> 
							FUENTES DE FINANCIACIÓN <strong>PROPIAS</strong> 
						</li>
					</ul>
					<ul>
						<li> 
							FUENTES DE FINANCIACIÓN <strong>AJENAS</strong>
						</li>
					</ul>
				</li>
				<li class="list-group-item">
					Otra clasificación puede ser la de 
					<ul>
						<li>
							FUENTES <strong>EXIGIBLES</strong>
						</li>
						<li>
							FUENTES <strong>NO</strong> EXIGIBLES
						</li>
					</ul> 
					lo que quiere decir que las primeras habrá que devolverlas, mientras que las segundas no
				</li>
			</ul>
		</div>
	</div>
	<div class="row well" id="fuentesP">
		<div class="col-xs-12 col-sm-12 col-md-12 col-lg-12">
			<h4>Fuentes PROPIAS de financiacion</h4>
			<ul class="list-group">
				<li class="list-group-item">
					Son aquellas que provienen de la propia empresa o de las aportaciones que hacen los socios. Todas ellas son no exigibles y forman parte del pasivo de la empresa en la masa Neto Patrimonial o Pasivo no Exigible
				</li>
				<li class="list-group-item">
					TIPOS:
					<ul>
						<li class="list-group-item">
							<strong>
								1.	Beneficios aplicados 
							</strong>
							<ul>
								<li>
									Las ganancias que obtiene la empresa al final de un ejercicio económico se pueden utilizar de diferente forma:
									<ul>
										<li>
											pago de impuestos (obligatorio)
										</li>
										<li>
											reparto de beneficios a los socios
										</li>
										<li>
											guardarlo sin aplicación alguna
										</li>
										<li>
											incluirlo en las reservas 
										</li>
										<li>
											reinvertirlo en la empresa
										</li>
										<li>...</li>
									</ul>
								</li>
								<li>
									Nosotros aquí nos fijamos en esta última utilidad que tiene como objeto renovar, amantener y ampliar los medios de la empresa para continuar funcionado
								</li>
								<li>
									Cuando la empresa marcha bien esta fuente puede ser la única que necesite la empresa, pero esto ocurre en muy contadas ocasiones
								</li>
							</ul>
							
						</li>
						<li class="list-group-item">
							<strong>
								2.	Beneficios no aplicados 
							</strong>
							<ul>
								<li>
									Es la parte de los beneficios que se han obtenido y que se  deposita de diferentes maneras que pueden tener diferentes denominaciones y aplicaciones, y años después se le puede dar un uso determinado
								</li>
								<li>
									Seran:
									<ul>
										<li>
											Reservas Legales
											<br>
											<small>
												Que son las que la ley obliga a tener a todas las sociedades distingue entre las Reservas legales, las estatutarias, además de otras voluntarias
											</small>
										</li>
										<li>
											Remanente
											<br>
											<small>
												Son beneficios que no se utilizan de momento
											</small>
										</li>
									</ul>
								</li>
							</ul>
						</li>

						<li class="list-group-item">
							<strong>
								3.	Dotaciones
							</strong>	
							<ul>
								<li>
									Son gastos que se hacen para cubrir ciertas situaciones
								</li>
								<li>
									Amortizaciones
									<ul>
										<li>
											Son fondos que la empresa va guardando para reponer inmovilizados materiales o inmaterial que dentro de un tiempo habrá que sustituir
										</li>
										<li>
											La cantidad que se amortiza cada año (dotación), se considera un gasto, y por ello contribuye a que la empresa deduzca dicha cantidad a la hora de pagar impuestos
										</li>
										<li>
											Tipos de amortizaciones: 
											<ul>
												<li>
													Lineal
												</li>
												<li>
													progresiva
												</li>
												<li>
													regresiva
												</li>
											</ul>
										</li>
									</ul>
								</li>
								<li>
									Provisiones
									<ul>
										<li>
											Son fondos que la empresa va guardando para hacer frente a situaciones desfavorables de riesgo previstas<small> aunque no confirmadas</small> que se pueden llegar a dar 
										</li>
										<li>
											y causen un perjuicio económico a la empresa <small>(clientes incobrables, siniestros, bajada de los precios de los productos que la empresa vende...)</small>
										</li>
										<li>
											Se consideran como gasto y se deducen a la hora de pagar impuestos
										</li>
									</ul>
								</li>
							</ul>
							
						</li>
						<li class="list-group-item">
							<strong>
								4.	Aportaciones iniciales de los socio y aportaciones posteriores (Capital y ampliaciones de capital)  
							</strong>	
							<ul>
								<li>
									Los accionistas antiguos tienen derecho preferente a adquirir las acciones nuevas de la ampliación, salvo que se establezca lo contrario
								</li>
								<li>
									La ampliación de capital es una fuente muy barata, ya que no hay que devolverla (es una fuente no exigible), pero puede crear malestar entre los accionistas, especialmente cuando se recurre a ella varias veces
								</li>
								<li>
									Solo las grandes empresas pueden hacer uso de esta fuente de financiación, ya que las PYMES no son atractivas para los inversores
								</li>
							</ul>
						</li>
					</ul>
				</li>
			</ul>

		</div>
	</div>

